    def parse_details(self, response):
        item = response.meta['item']
        name_regex = r'(Forward(.|\s*)Looking\s*Statements)(.|\s)*|(Franklin\s*Resources.\s*Inc.\s*(\(\s*NYSE\s*:\s*BEN\s*\)\s*)?is\s*a\s*global\s*investment\s*management)(.|\s)*'
        name_regex_2=r'(\bAbout\s*Franklin)(.|\s)*|(\bAbout.Franklin\b)(.|\s)*|(\bABOUT.Franklin\b)(.|\s)*|(\bABOUT\s*.FRANKLIN\b)(.|\s)*'
        if '.pdf' in response.url.lower() or 'external.file' in response.url.lower():
            item['file_urls'] = [response.url]
            item['DOCLINK'] = response.url
            item['DESCRIPTION'] = ''
            yield item
        else:
            item['DESCRIPTION'] = re.sub(name_regex,'' ," ".join(response.xpath('//div[@class="module_body"]//text()[not(ancestor::img)][not(ancestor::div[@class="box__right"] or self::style or self::script or  ancestor::style or ancestor::script or ancestor::p[@id="news-body-cta"] or ancestor::div[@id="bwbodyimg"])]').extract()), flags=re.IGNORECASE)
            item['DESCRIPTION'] = re.sub(name_regex_2,'' , item['DESCRIPTION'])
            item['DOCLINK'] = response.url
            pdf_link = response.xpath('//div[@class="module_links"]/a/@href').extract_first()
            if pdf_link:
              item['file_urls'] = [response.urljoin(pdf_link)]
            if not re.search('[a-zA-Z]', item['DESCRIPTION']):
                item['DESCRIPTION'] = 'FEHLER'
                yield item
            else:
                yield item
       
       