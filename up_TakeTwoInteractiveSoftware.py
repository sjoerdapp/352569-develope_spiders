    def parse_details(self, response):
        item = response.meta['item']
        name_regex = r'(Forward(.|\s*)Looking\s*Statements)(.|\s)*|(\bABOUT\s*Take\s*Two\b)(.|\s)*|(\bABOUT.Take.Two\b)(.|\s)*' #|(\bABOUT\s*L\s*BRANDS\b)(.|\s)*'
